package com.gunay.my_java_oop_notes;

public class Mathematics {

    //Static Polymorphism  - aynı sınıf içerisinde farklı metodları aynı isimle kullanma
    public int sum(){
        return 0;
    }

    public int sum(int x, int y){
        return x + y;
    }

    public int sum(int x, int y, int z){
        return x + y +z;
    }


}
